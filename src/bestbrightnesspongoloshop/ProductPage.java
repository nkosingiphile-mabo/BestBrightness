    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        String selectedSupplier = "";
       
        if(supplierList.getSelectedIndex() != -1)
        {
            selectedSupplier = supplierList.getSelectedItem();
        }

        if( selectedSupplier.isEmpty() || txtItemId.getText().isEmpty() || txtProduct.getText().isEmpty() || txtQuantity.getText().isEmpty() || txtSize.getText().isEmpty() || txtPrice.getText().isEmpty() || txtPrice.getText().isEmpty() || txtSellPrice.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Error!\nPlease Input All Fields!");
        }
        else
        {
            if(itemIdExist(txtItemId.getText()))
            {
                int newQuantity = Integer.parseInt(previousQuantity) + Integer.parseInt(txtQuantity.getText());
                db.update("UPDATE product SET quantity = '"+String.valueOf(newQuantity)+"' WHERE itemID = '"+txtItemId.getText()+"'");
                JOptionPane.showMessageDialog(null,"Success!\n\nItem(s) Added to Existing Stock");
            }
            else
            {
                String supplier = "Supplier";
                String supplierID = suppliers.get(supplierList.getSelectedIndex()).getSupplerID();
                String sql = "INSERT INTO product (itemID,supplierID,product,quantity,size,price,sellPrice,source) VALUES ('"+txtItemId.getText()+"','"+supplierID+"','"+txtProduct.getText()+"','"+txtQuantity.getText()+"','"+txtSize.getText()+"','"+txtPrice.getText()+"','"+txtSellPrice.getText()+"','"+supplier+"')";
                db.insert(sql);

                JOptionPane.showMessageDialog(null,"Congratulations!\nNew Item(s) Added In Stock!");
            }
            
            db.display("SELECT itemID,supplierID,product,quantity,size,price,sellPrice,source FROM product", stockTable);

            txtItemId.setText(null);
            txtProduct.setText(null);
            txtQuantity.setText(null);
            txtSize.setText(null);
            txtPrice.setText(null);
            txtSellPrice.setText(null);
            //JOptionPane.showMessageDialog(null,"Good To Go! "+ suppliers.get(supplierList.getSelectedIndex()).getSupplierName());
        }
    }//GEN-LAST:event_btnPlaceOrderActionPerformed

    private void btnStockInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStockInActionPerformed
        TableModel cell = storeroomTable.getModel();
        int rowIndex = storeroomTable.getSelectedRow();
     
        if(rowIndex == -1)
        {
            JOptionPane.showMessageDialog(null,"Please Select The Product You Want To Add To Products!");
        }
        else if (txtQuantity1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Please Enter The Quantity That You Want To Add To Products!");
        }
        else
        {
            if(Integer.parseInt(txtQuantity1.getText()) > Integer.parseInt(cell.getValueAt(rowIndex, 3).toString()))
            {
                JOptionPane.showMessageDialog(null,"ERROR!\nThe quantity you want to add cannot exceed the initial quantity!");
            }
            else
            {
                String itemID = cell.getValueAt(rowIndex, 0).toString();
                String supplierID = cell.getValueAt(rowIndex, 1).toString();
                String product = cell.getValueAt(rowIndex, 2).toString();
                String quantity = txtQuantity1.getText();
                String initialQuantity = cell.getValueAt(rowIndex, 3).toString();
                String size = cell.getValueAt(rowIndex, 4).toString();
                String price = cell.getValueAt(rowIndex, 5).toString();
                String sellPrice = cell.getValueAt(rowIndex, 6).toString();
                
                if(itemIdExist(itemID))
                {
                    int newQuantity = Integer.parseInt(previousQuantity) + Integer.parseInt(txtQuantity1.getText());
                    int storeroomQuantity = Integer.parseInt(initialQuantity) - Integer.parseInt(txtQuantity1.getText());
                    
                    db.update("UPDATE product SET quantity = '"+String.valueOf(newQuantity)+"' WHERE itemID = '"+itemID+"'");
                    db.update("UPDATE storeroom SET quantity = '"+String.valueOf(storeroomQuantity)+"' WHERE itemID = '"+itemID+"'");
                    
                    JOptionPane.showMessageDialog(null,"Success!\n\nItem(s) Added to Existing Stock");  
                }
                else
                {
                    String source = "Storeroom";
                    //String supplierID = suppliers.get(supplierList.getSelectedIndex()).getSupplerID();
                    String sql = "INSERT INTO product (itemID,supplierID,product,quantity,size,price,sellPrice,source) VALUES ('"+itemID+"','"+supplierID+"','"+product+"','"+quantity+"','"+size+"','"+price+"','"+sellPrice+"','"+source+"')";
                    db.insert(sql);

                    JOptionPane.showMessageDialog(null,"Congratulations!\nNew Item(s) Added In Stock!");
                }
            storeroomTable.clearSelection();
            txtQuantity1.setText(null);
            db.display("SELECT itemID,supplierID,product,quantity,size,price,sellPrice FROM storeroom", storeroomTable);
            db.display("SELECT itemID,supplierID,product,quantity,size,price,sellPrice,source FROM product", stockTable);

            }
        }

    }//GEN-LAST:event_btnStockInActionPerformed

