    public boolean itemIdExist(String itemId)
    {
        boolean exist = false;
        ResultSet rs = db.getData("SELECT * FROM product WHERE itemID ='"+itemId+"'");
        
        try{
           if(rs.next())
           {
               exist = true;
               previousQuantity = rs.getString("quantity");
           }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        return exist;
    }

