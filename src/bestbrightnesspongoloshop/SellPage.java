    public void fillCartTable()
    {
        DefaultTableModel tableModel = (DefaultTableModel) checkoutTable.getModel();
        tableModel.setRowCount(0);
        txtSlip.setText("------------------------------------------------------------\nBEST BRIGHTNESS PONGOLO SHOP\n-----------------------------------------------------------");
        
        for (CartProduct product : cartProducts)
        {
            Object[] rowData = {product.getItemID(), product.getProduct(), product.getSize(),product.getQuantity(),product.getUnitPrice(),product.getTotalPrice()};
            subtotal += Double.valueOf(product.getTotalPrice());
            tableModel.addRow(rowData);
            
            txtSlip.setText(txtSlip.getText()+"\n\n"+product.getProduct()+"\t"+product.getSize()+" @ "+product.getQuantity()+"\t "+product.getTotalPrice());
        }
        
        calculateSubtotal(cartProducts);
        subtotalLabel.setText("Subtotal: R"+subtotal);
        
        txtSlip.setText(txtSlip.getText() +"\n\n\nTOTAL: R"+subtotal+"\n------------------------------------------------------------");
    }
    
    public void calculateSubtotal(ArrayList<CartProduct> myArray)
    {
        subtotal = 0;
        
        for(CartProduct product : myArray)
        {
            subtotal += Double.valueOf(product.getTotalPrice());
        }
    }
    
    public void getSlip()
    {
        try{
             
            ResultSet rs = db.getData("SELECT slipContent FROM slip");
            while(rs.next())
            {
                slips.add(rs.getString("slipContent"));
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        txtSlipDisplay.setText(slips.get(0));
    }

