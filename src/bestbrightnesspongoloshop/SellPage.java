    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed
        TableModel cell = productTable.getModel();
        int rowIndex = productTable.getSelectedRow();
        
        if(rowIndex == -1)
        {
            JOptionPane.showMessageDialog(null,"Please Select The Product You Want To Add To Cart!");
        }
        else if (txtQuantity.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Please Enter The Quantity That You Want To Sell!");
        }
        else
        {
            if(Integer.parseInt(txtQuantity.getText()) > Integer.parseInt(cell.getValueAt(rowIndex, 3).toString()))
            {
                JOptionPane.showMessageDialog(null,"ERROR!\n\nThe quantity you want to sell cannot exceed the initial quantity!");
            }
            else
            {
                String itemID = cell.getValueAt(rowIndex, 0).toString();
                String product = cell.getValueAt(rowIndex, 1).toString();
                String size = cell.getValueAt(rowIndex, 2).toString();
                String initialQuantity = cell.getValueAt(rowIndex, 3).toString();
                String price = cell.getValueAt(rowIndex, 4).toString();
                String quantity = txtQuantity.getText();
                double totalPrice = 0;

                totalPrice = Double.valueOf(price) * Integer.valueOf(quantity);
                CartProduct obj = new CartProduct(itemID,product,size,initialQuantity,quantity,price,String.valueOf(totalPrice));

                cartProducts.add(obj);
                cartLabel.setText(cartProducts.size()+" item(s) in cart");
                productTable.clearSelection();
                txtQuantity.setText(null);
                fillCartTable();
                //JOptionPane.showMessageDialog(null,"Item(s) added to cart\nProceed to checkout when done!");
            }
        }
        
        
    }//GEN-LAST:event_btnAddToCartActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int choice = JOptionPane.showConfirmDialog(null,"Do you want to proceed to checkout?", "Confirmation",JOptionPane.YES_NO_OPTION);

        if (choice == JOptionPane.YES_OPTION) 
        {
            if(cartProducts.isEmpty())
            {
                 JOptionPane.showMessageDialog(null,"ERROR!\n\nCannot perform this operation when cart is empty!");
            }
            else
            {
                for(CartProduct product : cartProducts)
                {
                    String sql = "INSERT INTO sales (itemID,product,size,quantity,unitPrice,totalPrice) VALUES ('"+product.getItemID()+"','"+product.getProduct()+"','"+product.getSize()+"','"+product.getQuantity()+"','"+product.getUnitPrice()+"','"+product.getTotalPrice()+"')";
                    db.insert(sql);
                    int newProductQuantity = Integer.parseInt(product.getInitialQuantity()) - Integer.parseInt(product.getQuantity());
                    String sql2 = "UPDATE product SET quantity = '"+String.valueOf(newProductQuantity)+"' WHERE itemID = '"+product.getItemID()+"'";
                    db.update(sql2);
                }
                String sql = "INSERT INTO slip (slipContent) VALUES('"+txtSlip.getText()+"')";
                db.insert(sql);
                cartProducts.clear();
                fillCartTable();
                txtSlip.setText(null);
                JOptionPane.showMessageDialog(null,"THANK YOU!\n\nTransaction Completed!");
                db.display("SELECT itemID as ID,product as Product,size as Size,quantity as Qty ,sellPrice as price FROM product", productTable);
            }
        } 
        else 
        {
           
        }
    }//GEN-LAST:event_jButton1ActionPerformed

