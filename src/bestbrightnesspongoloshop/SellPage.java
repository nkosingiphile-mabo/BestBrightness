    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        TableModel cell = checkoutTable.getModel();
        int rowIndex = checkoutTable.getSelectedRow();
        
        if(txtCheckoutQuantity.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"ERROR!\n\nPlease Select An Item!");
        }
        else if(Integer.parseInt(txtCheckoutQuantity.getText()) > Integer.parseInt(cartProducts.get(rowIndex).getInitialQuantity()))
        {
            JOptionPane.showMessageDialog(null,"The quantity you want to sell cannot exceed the initial quantity!");
        }
        else
        {
            CartProduct obj = new CartProduct();
            obj = cartProducts.get(rowIndex);
            
            obj.setQuantity(txtCheckoutQuantity.getText());
            
            cartProducts.set(rowIndex,obj);
        }
        fillCartTable();
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

