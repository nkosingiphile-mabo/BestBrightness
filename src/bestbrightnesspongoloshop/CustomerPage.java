    private void createCustomerAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createCustomerAccountActionPerformed
       if(txtCustomerId.getText().isEmpty() || txtName.getText().isEmpty() || txtSurname.getText().isEmpty() || txtPhone.getText().isEmpty() || txtEmail.getText().isEmpty() || txtAddress.getText().isEmpty())
       {
           JOptionPane.showMessageDialog(null,"Error!\nPlease Input All Fields!");
       }
       else
       {
           String sql = "INSERT INTO customer (ID,CName,Surname,Phone,Email,Address) VALUES ('"+txtCustomerId.getText()+"','"+txtName.getText()+"','"+txtSurname.getText()+"','"+txtPhone.getText()+"','"+txtEmail.getText()+"','"+txtAddress.getText()+"')";
           db.insert(sql);
           
           JOptionPane.showMessageDialog(null,"Congratulations!\nCustomer Account Created!");
           
           db.display("SELECT ID as Cust_ID,CName as Name, Surname,Phone,Email,Address FROM customer", customerTable);
           
           txtCustomerId.setText(null);
           txtName.setText(null);
           txtSurname.setText(null);
           txtPhone.setText(null);
           txtEmail.setText(null);
           txtAddress.setText(null);
       }
    }//GEN-LAST:event_createCustomerAccountActionPerformed

